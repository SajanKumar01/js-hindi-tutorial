const months = 3;
switch (months) {
  case 1:
    console.log("january");
    break;

  case 2:
    console.log("feb");
    break;

  case 3:
    console.log("march");
    break;

  case 4:
    console.log("April");
    break;

  case 5:
    console.log("may");
    break;
  case 6:
    console.log("june");
    break;
  case 7:
    console.log("july");
    break;
  case 8:
    console.log("Augest");
    break;

  case 9:
    console.log("September");
    break;

  case 10:
    console.log("octover");
    break;
  case 11:
    console.log("Nomber");
    break;
  case 12:
    console.log("December");
    break;
  default:
    console.log("Default case match");
    break;
}
